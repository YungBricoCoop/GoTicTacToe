// Width returns the width of the map in tiles.
func (m Map) Width() int {
	if len(m.Tiles) == 0 {
		return 0
	}
	return len(m.Tiles[0])
}

// Height returns the height of the map in tiles.
func (m Map) Height() int {
	return len(m.Tiles)
}

// IsWalkable returns true if the given position is walkable (not a wall).
func (m Map) IsWalkable(pos Vec2) bool {
	x, y := int(pos.X), int(pos.Y)
	if x < 0 || x >= m.Width() || y < 0 || y >= m.Height() {
		return false
	}
	return m.Tiles[y][x] == 0 //FIXME: define variable for each tile type
}

// GetTile returns the tile value at the given (x, y) coordinates.
func (m Map) GetTile(x, y int) uint8 {
	if y < 0 || y >= m.Height() || x < 0 || x >= m.Width() {
		return 1 // out of bounds is wall
	}
	return m.Tiles[y][x]
}
